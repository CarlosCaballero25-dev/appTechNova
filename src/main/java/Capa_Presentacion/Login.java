    private Usuario verificarCredenciales(String usuarioInput, String passwordInput, String rolSeleccionado) {
        List<Usuario> usuariosRegistrados = obtenerUsuariosRegistrados();

        for (Usuario usuario : usuariosRegistrados) {
            // Compara el nombre de usuario y la contraseña
            if (usuario.getUsuario().equals(usuarioInput) && usuario.getPassword().equals(passwordInput)) {
                // Verifica si el rol seleccionado coincide con el rol del usuario
                if (usuario.getRol().equalsIgnoreCase(rolSeleccionado)) {
                    return usuario; // Devuelve el usuario verificado
                } else {
                    JOptionPane.showMessageDialog(this, "El rol seleccionado no coincide con el rol del usuario.", "Error", JOptionPane.ERROR_MESSAGE);
                    return null; // El rol no coincide
                }
            }
        }
        return null; // Si no se encuentra el usuario
    }

// Este método es solo un ejemplo. Debes implementar la lógica de obtención real de usuarios.
    private List<Usuario> obtenerUsuariosRegistrados() {
        UsuarioDAO usuarioDAO = new UsuarioDAO(); // Crea una instancia del DAO
        return usuarioDAO.listarUsuarios(); // Llama al método para obtener la lista de usuarios
    }

