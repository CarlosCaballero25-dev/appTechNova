    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // Obtener datos del usuario y la contraseña
        String usuario = txtUsuario.getText();
        String password = String.valueOf(txtPassword.getPassword());
        String rolSeleccionado = (String) cboRol.getSelectedItem(); // Obtener el rol seleccionado

        // Verificar las credenciales del usuario
        Usuario usuarioVerificado = verificarCredenciales(usuario, password, rolSeleccionado);

        if (usuarioVerificado != null) {
            // Si el usuario es verificado, mostrar el menú
            java.awt.EventQueue.invokeLater(() -> {
                new frmMenu(usuarioVerificado).setVisible(true); // Asegúrate de pasar el usuario
            });
            dispose(); // Cierra la ventana de login
        } else {
            // Mostrar mensaje de error
            JOptionPane.showMessageDialog(this, "Usuario o contraseña incorrectos.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

